  <h2>Temperature Measurement</h2>
  <p>Thermometers are commonly used to take the temperature of a liquid. Parallax errors can occur when reading off a thermometer.</p>
  <h3>Parallax Error</h3>
  <p>A parallax error occurs when the reading is taken inaccurately due to the thermometer being viewed at a bad angle. This error can be avoided by viewing the thermometer at a <strong>perpendicular</strong> angle.</p>
