  <h1>Environmental Risks</h1>
  <h2>Soil Erosion</h2>
  <p>Soil erosion is the wearing away fo topsoil by wind or water.</p>
  <h3>Deforesation</h3>
  <p>The removal of trees weakens the soil. Deforestation:</p>
  <ul>
    <li>decreases interception which makes the soil more exposed to rainfall</li>
    <li>decreases the number of roots which decreases infiltration and increases surface run-off</li>
    <li>leaves <strong>bare soil more exposed</strong> to wind due to the lack of tree cover</li>
  </ul>
  <Callout emoji="âš ">
    Mentioning loss of nutrients alone is not creditworthy.
  </Callout>
  <h3>Overgrazing</h3>
  <p>Livestock farming is major reason for overgrazing. Overgrazing:</p>
  <ul>
    <li>removes vegetation cover</li>
    <li>means more animals will be trampling land which will lead to compaction and increased surface run-off</li>
  </ul>
  <h3>Overcultivation</h3>
  <p>The land is not left to rest between crops. This reduces the fertility of the soil.</p>
  <h3>Climate Change</h3>
  <p>Changes in climate patterns and extreme weather events like floods and droughts lead to soil erosion.</p>
  <h2>Desertification</h2>
  <p>Desertification is a type of land degradation that occurs in dry areas. It reduces the biological productivity of the land. It is strongly linked to soil erosion.</p>
  <h2>Pollution</h2>
  <h3>Water Pollution</h3>
  <p>Water pollution often occurs in rivers. Common causes of river pollution include:</p>
  <ul>
    <li>fertiliser</li>
    <li>pesticide</li>
    <li>human waste</li>
  </ul>
  <p><strong>Eutrophication</strong> occurs when there are excess nutrients in the water due to the use of fertilisers. It usually <strong>decreases the concentration of oxygen in water</strong> and kills fish.</p>
  <ul>
    <li>Death of organisms leads to <strong>reduced biodiversity</strong></li>
    <li>Death of fish leads to reduction in food sources for predators</li>
  </ul>
  <p>Many pollutants in rivers are poisonous to aquatic life. Consuming pollutants can lead to an increase in the concentration of pollutants higher up the food chain. For example, swordfish meat contains high levels of mercury which can pose a threat to the nervous system.</p>
