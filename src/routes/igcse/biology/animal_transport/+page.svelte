  <h1>Animal Transport</h1>
  <h2>Circulatory System</h2>
  <p>The circulatory system is a system of blood vessels and valves.</p>
  <ul>
    <li>Valves prevent the backflow of blood</li>
    <li>Most mammals have a double circulatory system</li>
  </ul>
  <h3>Double Circulation</h3>
  <p>Circulation occurs to both the lungs and to the body tissues in mammals.</p>
  <ul>
    <li>Blood is pumped more slowly to the lungs to not damange the delicate lung tissues</li>
    <li>Blood is pumped more quickly to the rest of the body to supply organs with oxygen more frequently</li>
  </ul>
  <h2>Blood Components</h2>
  <p>Blood is made up of 3 main components:</p>
  <ul>
    <li>red blood cells</li>
    <li>white blood cells</li>
    <li>platelets</li>
  </ul>
  <p>These components are carried by <strong>plasma</strong> which also contains nutrients and waste products such as:</p>
  <ul>
    <li>glucose</li>
    <li>amino acids</li>
    <li>carbon dioxide</li>
    <li>urea</li>
  </ul>
  <h3>Red Blood Cell</h3>
  <p>A red blood cell is a specialised cell that contains <strong>haemoglobin</strong> in its cytoplasm. Haemoglobin binds to oxygen during gas exchange. This helps red blood cells transport oxygen and deliver it to organs for respiration.</p>
  <h3>White Blood Cell</h3>
  <p>A white blood cell is a type of cell that is part of the immune system. White blood cells play a major role in:</p>
  <ul>
    <li>
      <strong>phagocytosis</strong>: the process of ingesting and killing foreign microorganisms
    </li>
    <li>
      <strong>antibody production</strong>: the process of releasing antibodies to slow the movement of pathogenic cells
    </li>
  </ul>
